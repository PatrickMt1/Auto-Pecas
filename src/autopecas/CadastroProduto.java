    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        // TODO add your handling code here:
        atualizaCampos();
        lbImagem.setIcon(null);
        lbImagem.setText("");
        BufferedImage bi;
        String sql = "select * from produto where codigo = " + tfCodigo.getText();
        produtoDAO.pesquisarProduto(sql);
        /* tentativa da pesquisa no banco de dados */
        try {
            produtoDAO.resultSet.first();
            urlFoto = produtoDAO.resultSet.getString("imagem");
            /* tentativa de colocar a imagem na tela */
            try {
                arquivoFoto = new File(urlFoto);
                //carrega a imagem real num buffer
                bi = ImageIO.read(arquivoFoto);
                //cria um buffer auxiliar com o tamanho desejado
                BufferedImage aux = new BufferedImage(165, 140, bi.getType());
                //pega a classe graphics do aux para edicao
                Graphics2D g = aux.createGraphics();
                //cria a transformacao
                AffineTransform at = AffineTransform.getScaleInstance((double) 165 / bi.getWidth(), (double) 140 / bi.getHeight());
                //pinta e transforma a imagem real no auxiliar
                g.drawRenderedImage(bi, at);
                //seta no jlabel
                lbImagem.setIcon(new ImageIcon(aux));
                lbImagem.setText(null);
                imagemEscolhida = aux;
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(null, "Imagem não encontrada no arquivo .");
            }
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Imagem não encontrada no banco.");
        }
        produtoDAO.produto.setCodigo(tfCodigo.getText());
        produtoDAO.produto.setNome(tfNome.getText());
        produtoDAO.produto.setCarrosCompativeis(cbCarrosCompa.getSelectedItem().toString());
        produtoDAO.produto.setFornecedor(cbFornecedor.getSelectedItem().toString());
        produtoDAO.produto.setMarca(cbMarca.getSelectedItem().toString());
        produtoDAO.produto.setImagem(urlFoto);
        produtoDAO.produto.setCodigoFornecedor(tfCodigoFornecedor.getText());
        String valorV = FormataV(tfPreco.getText());
        produtoDAO.produto.setPreco(valorV);


    }//GEN-LAST:event_btBuscarActionPerformed

