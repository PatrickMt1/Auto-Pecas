    public void atualizaCampos() {
        produtoDAO.produto.setCodigo(tfCodigo.getText());
        if (produtoDAO.localizarProduto()) {
            tfCodigo.setText(produtoDAO.produto.getCodigo());
            tfNome.setText(produtoDAO.produto.getNome());
            cbCarrosCompa.setSelectedItem(produtoDAO.produto.getCarrosCompativeis());
            cbFornecedor.setSelectedItem(produtoDAO.produto.getFornecedor());
            cbMarca.setSelectedItem(produtoDAO.produto.getMarca());
            tfQuantidade.setText(produtoDAO.produto.getQuantidade());
            tfPreco.setText(produtoDAO.produto.getPreco());

        } else {
            JOptionPane.showMessageDialog(null, "Produto não encontrado.");
            limpaCampos();
        }
    }

    public void preencherTabela(String sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Código", "Nome do Produto", "Carros Compatíveis", "Fornecedor", "Marca", "Quantidade", "Preço", "Código do Fornecedor"};
        produtoDAO.pesquisarProduto(sql);
        try {
            produtoDAO.resultSet.first();
            do {

                /* adicionando os dados que vem do banco para a tabela */
                String FormataT = FormataTabela(produtoDAO.resultSet.getString("preco"));
                dados.add(new Object[]{
                    produtoDAO.resultSet.getInt("codigo"),
                    produtoDAO.resultSet.getString("nome"),
                    produtoDAO.resultSet.getString("carrosCompativeis"),
                    produtoDAO.resultSet.getString("fornecedor"),
                    produtoDAO.resultSet.getString("marca"),
                    produtoDAO.resultSet.getString("quantidade"),
                    FormataT,
                    produtoDAO.resultSet.getString("fornecedores_codigo"),});
            } while (produtoDAO.resultSet.next());
        } catch (SQLException erro) {
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTProduto.setModel(modelo);
        jTProduto.getColumnModel().getColumn(0).setPreferredWidth(80);
        jTProduto.getColumnModel().getColumn(0).setResizable(false);
        jTProduto.getColumnModel().getColumn(1).setPreferredWidth(220);  // define o tamanho das colunas e se será redimensionado ou não
        jTProduto.getColumnModel().getColumn(1).setResizable(true);  // não permite alterar o tamanho da coluna
        jTProduto.getColumnModel().getColumn(2).setPreferredWidth(120);
        jTProduto.getColumnModel().getColumn(2).setResizable(false);
        jTProduto.getColumnModel().getColumn(3).setPreferredWidth(150);
        jTProduto.getColumnModel().getColumn(3).setResizable(true);
        jTProduto.getColumnModel().getColumn(4).setPreferredWidth(150);
        jTProduto.getColumnModel().getColumn(4).setResizable(false);
        jTProduto.getColumnModel().getColumn(5).setPreferredWidth(150);
        jTProduto.getColumnModel().getColumn(5).setResizable(false);
        jTProduto.getColumnModel().getColumn(6).setPreferredWidth(150);
        jTProduto.getColumnModel().getColumn(6).setResizable(false);
        jTProduto.getColumnModel().getColumn(7).setPreferredWidth(150);
        jTProduto.getColumnModel().getColumn(7).setResizable(false);
        jTProduto.getTableHeader().setReorderingAllowed(false);  // Não permite reordenar as colunas
        jTProduto.setAutoResizeMode(jTProduto.AUTO_RESIZE_OFF); // Não permite redimensionar a tabela
        jTProduto.setSelectionMode(ListSelectionModel.SINGLE_SELECTION); // permite selecionar apenas 1 elemento da tabela
        produtoDAO.conecta.fecharBD();
    }
