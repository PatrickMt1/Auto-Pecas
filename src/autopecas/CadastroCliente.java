    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        // TODO add your handling code here:
        if (tfCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Insira o Código do Cliente");
            tfCodigo.requestFocus();
            if (!tfCodigo.getText().equals("")) {
                clienteDAO.cliente.setCodigo(tfCodigo.getText());
                if (clienteDAO.localizarCliente()) {
                    tfCodigo.setText(clienteDAO.cliente.getCodigo());
                    tfNome.setText(clienteDAO.cliente.getNome());
                    tfEmail.setText(clienteDAO.cliente.getEmail());
                    if (clienteDAO.cliente.getSexo().equals("M")) {
                        rbMasc.setSelected(true);
                    } else {
                        rbFem.setSelected(true);
                    }
                    if (clienteDAO.cliente.getPessoa().equals("Física")) {
                        rbPessoaFisica.setSelected(true);
                        ftCpf.setText(clienteDAO.cliente.getCpf());
                        ftRg.setText(clienteDAO.cliente.getRg());
                        limpacamposFisico();
                        camposSexo(true, true);
                        camposPessoa(true, false, true);
                    } else {
                        rbPessoaJuridica.setSelected(true);
                        ftCnpj.setText(clienteDAO.cliente.getCnpj());
                        limpacamposJuridico();
                        camposSexo(false, false);
                        camposPessoa(false, true, false);
                    }
                    ftTelefone.setText(clienteDAO.cliente.getTelefone());
                    ftCelular.setText(clienteDAO.cliente.getCelular());
                    tfEndereco.setText(clienteDAO.cliente.getEndereco());
                    tfCidade.setText(clienteDAO.cliente.getCidade());
                    ftCep.setText(clienteDAO.cliente.getCep());
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Cliente não encontrado");
            atualizaCampos();
        }
    }//GEN-LAST:event_btBuscarActionPerformed

