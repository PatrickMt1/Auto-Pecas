    private void jtClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtClienteMouseClicked
        // TODO add your handling code here:
        int linhaSelecionada = jtCliente.getSelectedRow();
        if (linhaSelecionada >= 0) {
            tfCodigo.setText(jtCliente.getValueAt(linhaSelecionada, 0).toString());
            tfNome.setText(jtCliente.getValueAt(linhaSelecionada, 1).toString());
            tfEmail.setText(jtCliente.getValueAt(linhaSelecionada, 2).toString());
            String sexoSelecionado = (jtCliente.getValueAt(linhaSelecionada, 3).toString());
            if ("M".equals(sexoSelecionado)) {
                rbMasc.setSelected(true);
            } else {
                rbFem.setSelected(true);
            }
            String fjSelecionado = (jtCliente.getValueAt(linhaSelecionada, 4).toString());
            if ("Física".equals(fjSelecionado)) {
                rbPessoaFisica.setSelected(true);
                ftCpf.setText(jtCliente.getValueAt(linhaSelecionada, 5).toString());
                ftRg.setText(jtCliente.getValueAt(linhaSelecionada, 6).toString());
                limpacamposFisico();
                camposPessoa(true, false, true);
            } else {
                rbPessoaJuridica.setSelected(true);
                ftCnpj.setText(jtCliente.getValueAt(linhaSelecionada, 7).toString());
                limpacamposJuridico();
                camposPessoa(false, true, false);
            }
            ftTelefone.setText(jtCliente.getValueAt(linhaSelecionada, 8).toString());
            ftCelular.setText(jtCliente.getValueAt(linhaSelecionada, 9).toString());
            tfEndereco.setText(jtCliente.getValueAt(linhaSelecionada, 10).toString());
            tfCidade.setText(jtCliente.getValueAt(linhaSelecionada, 11).toString());
            ftCep.setText(jtCliente.getValueAt(linhaSelecionada, 12).toString());
        }
    }//GEN-LAST:event_jtClienteMouseClicked

