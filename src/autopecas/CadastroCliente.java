    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        if (tfCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o código do cliente a ser excluido.");
            tfCodigo.requestFocus();
            return;
        }
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja Excluir?", "ALERTA", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (resposta == 0) {
            clienteDAO.cliente.setCodigo("" + tfCodigo.getText());
            clienteDAO.excluirAgendamento();
            clienteDAO.excluirCarro();
            JOptionPane.showMessageDialog(null, clienteDAO.excluirCliente());
            limpaCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Cliente não excluido.");
        }
    }//GEN-LAST:event_btExcluirActionPerformed

