    public void limpaCampos() {
        tfCodigo.setText("");
        tfNome.setText("");
        tfEmail.setText("");
        rbMasc.setSelected(true);
        rbFem.setSelected(false);
        rbPessoaFisica.setSelected(false);
        rbPessoaJuridica.setSelected(false);
        ftCnpj.setText("");
        ftCpf.setText("");
        ftRg.setText("");
        ftTelefone.setText("");
        ftCelular.setText("");
        tfEndereco.setText("");
        tfCidade.setText("");
        ftCep.setText("");
    }

    public void atualizaCampos() {
        clienteDAO.cliente.setCodigo(tfCodigo.getText());
        if (clienteDAO.localizarCliente()) {
            clienteDAO.cliente.setCodigo(tfCodigo.getText());
            clienteDAO.cliente.setNome(tfNome.getText());
            clienteDAO.cliente.setEmail(tfEmail.getText());
            if (rbMasc.isSelected()) {
                clienteDAO.cliente.setSexo("M");
            } else if (rbFem.isSelected()) {
                clienteDAO.cliente.setSexo("F");
            }
            if (rbPessoaFisica.isSelected()) {
                clienteDAO.cliente.setPessoa("Física");
            } else if (rbPessoaJuridica.isSelected()) {
                clienteDAO.cliente.setPessoa("Jurídica");
            }
            clienteDAO.cliente.setCpf(ftCpf.getText());
            clienteDAO.cliente.setRg(ftRg.getText());
            clienteDAO.cliente.setCnpj(ftCnpj.getText());
            clienteDAO.cliente.setTelefone(ftTelefone.getText());
            clienteDAO.cliente.setCelular(ftCelular.getText());
            clienteDAO.cliente.setEndereco(tfEndereco.getText());
            clienteDAO.cliente.setCidade(tfCidade.getText());
            clienteDAO.cliente.setCep(ftCep.getText());
        } else {
            limpaCampos();
        }
    }

