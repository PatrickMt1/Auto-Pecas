    public void atualizaCampos() {
        carroDAO.carro.setPlaca(ftPlaca.getText());
        if (carroDAO.localizarCarro()) {
            ftPlaca.setText(carroDAO.carro.getPlaca());
            cbMarca.setSelectedItem(carroDAO.carro.getMarca());
            tfNome.setText(carroDAO.carro.getNome());
            tfModelo.setText(carroDAO.carro.getModelo());
        } else {
            JOptionPane.showMessageDialog(null, "Carro não encontrado.");
            limpaCampos();
        }
    }

    public void preencherTabela(String sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Placa", "Código do Cliente", "Marca", "Nome", "Ano/Modelo"};
        carroDAO.pesquisarCarro(sql);
        try {
            carroDAO.resultSet.first();
            do {
                dados.add(new Object[]{
                    carroDAO.resultSet.getString("placa"),
                    carroDAO.resultSet.getString("fkCliente"),
                    carroDAO.resultSet.getString("marca"),
                    carroDAO.resultSet.getString("nome"),
                    carroDAO.resultSet.getString("modelo"),});
            } while (carroDAO.resultSet.next());
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Erro: \n" + erro);
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jTCarros.setModel(modelo);
        jTCarros.getColumnModel().getColumn(0).setPreferredWidth(100);
        jTCarros.getColumnModel().getColumn(0).setResizable(false);
        jTCarros.getColumnModel().getColumn(1).setPreferredWidth(100);
        jTCarros.getColumnModel().getColumn(1).setResizable(true);
        jTCarros.getColumnModel().getColumn(2).setPreferredWidth(120);
        jTCarros.getColumnModel().getColumn(2).setResizable(false);
        jTCarros.getColumnModel().getColumn(3).setPreferredWidth(120);
        jTCarros.getColumnModel().getColumn(3).setResizable(false);
        jTCarros.getColumnModel().getColumn(4).setPreferredWidth(120);
        jTCarros.getColumnModel().getColumn(4).setResizable(false);
        jTCarros.getTableHeader().setReorderingAllowed(false);
        jTCarros.setAutoResizeMode(jTCarros.AUTO_RESIZE_OFF);
        jTCarros.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        carroDAO.conecta.fecharBD();
    }

