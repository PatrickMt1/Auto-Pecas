        if (tfModelo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Modelo.");
            tfModelo.requestFocus();
            return;
        }
        carroDAO.carro.setPlaca(ftPlaca.getText());
        carroDAO.carro.setMarca(cbMarca.getSelectedItem().toString());
        carroDAO.carro.setNome(tfNome.getText());
