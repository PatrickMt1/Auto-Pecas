    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        // TODO add your handling code here:
        atualizaCampos();
        if (!ftPlaca.getText().equals("")) {
            carroDAO.carro.setPlaca(ftPlaca.getText());
            if (carroDAO.localizarCarro()) {
                ftPlaca.setText(carroDAO.carro.getPlaca());
                tfCodigoCliente.setText(carroDAO.carro.getCodigoCliente());
                cbMarca.setSelectedItem(carroDAO.carro.getMarca());
                tfNome.setText(carroDAO.carro.getNome());
                tfModelo.setText(carroDAO.carro.getModelo());
            }
        }
    }//GEN-LAST:event_btBuscarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        // TODO add your handling code here:
        if (ftPlaca.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite a placa e o carro será excluído.");
            ftPlaca.requestFocus();
            return;
        }
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja Excluir?", "ALERTA", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (resposta == 0) {
            carroDAO.carro.setPlaca("" + ftPlaca.getText());
            carroDAO.excluirCliente();
            JOptionPane.showMessageDialog(null, carroDAO.excluirCarro());
            limpaCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Carro não excluido.");
        }
    }//GEN-LAST:event_btExcluirActionPerformed

