    public void preencherTabela(String sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"CÓDIGO", "HORA DO SERVIÇO", "DATA DO SERVIÇO", "DATA DO AGENDAMENTO", "HORA DO AGENDAMENTO", "Código Veículo", "Código Cliente"};
        agendaDAO.pesquisarAgenda(sql);
        try {
            agendaDAO.resultSet.first();
            do {
                dados.add(new Object[]{
                    agendaDAO.resultSet.getString("codigoAgendada"),
                    agendaDAO.resultSet.getString("horaServico"),
                    agendaDAO.resultSet.getString("dataServico"),
                    agendaDAO.resultSet.getString("dataAgendada"),
                    agendaDAO.resultSet.getString("horaAgendada"),
                    agendaDAO.resultSet.getString("fkCarro"),
                    agendaDAO.resultSet.getString("fkCliente"),});
            } while (agendaDAO.resultSet.next());
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Erro: \n" + erro);
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jtAgenda.setModel(modelo);
        jtAgenda.getColumnModel().getColumn(0).setPreferredWidth(80);
        jtAgenda.getColumnModel().getColumn(0).setResizable(false);
        jtAgenda.getColumnModel().getColumn(1).setPreferredWidth(100);
        jtAgenda.getColumnModel().getColumn(1).setResizable(true);
        jtAgenda.getColumnModel().getColumn(2).setPreferredWidth(100);
        jtAgenda.getColumnModel().getColumn(2).setResizable(false);
        jtAgenda.getColumnModel().getColumn(3).setPreferredWidth(100);
        jtAgenda.getColumnModel().getColumn(3).setResizable(false);
        jtAgenda.getColumnModel().getColumn(4).setPreferredWidth(100);
        jtAgenda.getColumnModel().getColumn(4).setResizable(false);
        jtAgenda.getColumnModel().getColumn(5).setPreferredWidth(100);
        jtAgenda.getColumnModel().getColumn(5).setResizable(false);
        jtAgenda.getColumnModel().getColumn(6).setPreferredWidth(100);
        jtAgenda.getColumnModel().getColumn(6).setResizable(false);
        jtAgenda.getTableHeader().setReorderingAllowed(false);
        jtAgenda.setAutoResizeMode(jtAgenda.AUTO_RESIZE_OFF);
        jtAgenda.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        agendaDAO.conecta.fecharBD();
    }

