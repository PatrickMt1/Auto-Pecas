    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        // TODO add your handling code here:
        Timer timer = new Timer(1000, new CadastroAgenda.timer());
        timer.stop();
        if (ftCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Insira o código do agendamento");

            if (!ftCodigo.getText().equals("")) {
                agendaDAO.agenda.setCodigoAgendada(ftCodigo.getText());
                if (agendaDAO.localizarAgendamento()) {
                    ftCodigo.setText(agendaDAO.agenda.getCodigoAgendada());
                    ftHoraServico.setText(agendaDAO.agenda.getHoraServico());
                    ftDataServico.setText(agendaDAO.agenda.getDataServico());
                    ftDataAgendada.setText(agendaDAO.agenda.getDataAgendada());
                    ftHoraAgendada.setText(agendaDAO.agenda.getHoraAgendada());
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Funcionário não encontrado");
            atualizaCampos();
        }


    }//GEN-LAST:event_btBuscarActionPerformed

    private void btNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btNovoActionPerformed
        // TODO add your handling code here:
        String codigoNovo;
        limpaCamposNovo(false, false);
        codigoNovo = "" + agendaDAO.proximoAgendamento();
        if (!codigoNovo.equals("0")) {
            ftCodigo.setText(codigoNovo);
        }
        ftHoraServico.requestFocus();
        Timer timer = new Timer(1000, new CadastroAgenda.timer());
        timer.start();
        campoDesa(false);
    }//GEN-LAST:event_btNovoActionPerformed

    private void btGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGravarActionPerformed
        // TODO add your handling code here:
        if (ftCodigo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Código.");
            ftCodigo.requestFocus();
            return;
        }
        if (ftHoraServico.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Hora do Serviço.");
            ftHoraServico.requestFocus();
            return;
        }
        if (ftDataServico.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Data do Serviço.");
            ftDataServico.requestFocus();
            return;
        }
        if (ftDataAgendada.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Data do Agendamento.");
            ftDataAgendada.requestFocus();
            return;
        }
        if (ftHoraAgendada.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Hora do Agendamento.");
            ftHoraAgendada.requestFocus();
            return;
        }
        if (tfCodigoVeiculo.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Código do Veiculo.");
            tfCodigoVeiculo.requestFocus();
            return;
        }
        if (tfCodigoCliente.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Código do Cliente.");
            tfCodigoCliente.requestFocus();
            return;
        }
        agendaDAO.agenda.setCodigoAgendada(ftCodigo.getText());
        agendaDAO.agenda.setHoraServico(ftHoraServico.getText());
        String dataEN = FormatDateEN(ftDataServico.getText());
        agendaDAO.agenda.setDataServico(dataEN);
        String dataEN2 = FormatDateEN(ftDataAgendada.getText());
        agendaDAO.agenda.setDataAgendada(dataEN2);
        agendaDAO.agenda.setHoraAgendada(ftHoraAgendada.getText());
        agendaDAO.agenda.setFkCarro(tfCodigoVeiculo.getText());
        agendaDAO.agenda.setFkCliente(tfCodigoCliente.getText());
        JOptionPane.showMessageDialog(null, agendaDAO.inserirAgendamento());
    }//GEN-LAST:event_btGravarActionPerformed

