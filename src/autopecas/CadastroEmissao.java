    public void preencherTabela(String sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"CÓDIGO", "NOME DO CLIENTE", "VALOR", "DATA", "VENDEDOR", "TIPO DE PGTO", "TEM OS","CLIENTE CODIGO","CODIGO OS"};
        emissaoDAO.pesquisarNota(sql);
        try {
            emissaoDAO.resultSet.first();
            do {
                String valorP = FormataTabela(emissaoDAO.resultSet.getString("valor"));
                String dataBR = formataDataBR(emissaoDAO.resultSet.getString("data"));
                dados.add(new Object[]{
                    emissaoDAO.resultSet.getInt("codigo"),
                    
                    
                    emissaoDAO.resultSet.getString("nomeCliente"),
                    valorP,
                    dataBR,
                    emissaoDAO.resultSet.getString("vendedor"),
                    emissaoDAO.resultSet.getString("tipoPgto"),
                    emissaoDAO.resultSet.getString("has_OS"),
                    emissaoDAO.resultSet.getString("cliente_codigo"),
                    emissaoDAO.resultSet.getString("OS_codigo"),});
            } while (emissaoDAO.resultSet.next());
        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Erro: \n" + erro);
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        jtNotaFiscal.setModel(modelo);
        jtNotaFiscal.getColumnModel().getColumn(0).setPreferredWidth(60);
        jtNotaFiscal.getColumnModel().getColumn(0).setResizable(false);
        jtNotaFiscal.getColumnModel().getColumn(1).setPreferredWidth(200);
        jtNotaFiscal.getColumnModel().getColumn(1).setResizable(true);
        jtNotaFiscal.getColumnModel().getColumn(2).setPreferredWidth(150);
        jtNotaFiscal.getColumnModel().getColumn(2).setResizable(false);
        jtNotaFiscal.getColumnModel().getColumn(3).setPreferredWidth(150);
        jtNotaFiscal.getColumnModel().getColumn(3).setResizable(false);
        jtNotaFiscal.getColumnModel().getColumn(4).setPreferredWidth(150);
        jtNotaFiscal.getColumnModel().getColumn(4).setResizable(false);
        jtNotaFiscal.getColumnModel().getColumn(5).setPreferredWidth(130);
        jtNotaFiscal.getColumnModel().getColumn(5).setResizable(false);
        jtNotaFiscal.getColumnModel().getColumn(6).setPreferredWidth(80);
        jtNotaFiscal.getColumnModel().getColumn(6).setResizable(false);
        jtNotaFiscal.getColumnModel().getColumn(7).setPreferredWidth(80);
        jtNotaFiscal.getColumnModel().getColumn(7).setResizable(false);
        jtNotaFiscal.getColumnModel().getColumn(8).setPreferredWidth(80);
        jtNotaFiscal.getColumnModel().getColumn(8).setResizable(false);
        jtNotaFiscal.getTableHeader().setReorderingAllowed(false);
        jtNotaFiscal.setAutoResizeMode(jtNotaFiscal.AUTO_RESIZE_OFF);
        jtNotaFiscal.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        emissaoDAO.conecta.fecharBD();
    }

