    private void btDemitirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDemitirActionPerformed
        // TODO add your handling code here:
        if (tfMatricula.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Matrícula.");
            tfMatricula.requestFocus();;
            return;
        }
        // Função OK_CANCEL_OPTION  para colocar um janela com opções "SIM" para excuir e "NÂO" para não exlcuir
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja Excluir este Funcionario ?", "", JOptionPane.OK_CANCEL_OPTION);
        if (resposta == 0) {
            funcionarioDAO.funcionario.setMatricula(tfMatricula.getText());
            funcionarioDAO.excluiOS();
            JOptionPane.showMessageDialog(null, funcionarioDAO.excluiFuncionario());
            limpaCampos();
            String sql = "select * from funcionario";
            //preecherTabela(sql);

        }
    }//GEN-LAST:event_btDemitirActionPerformed

