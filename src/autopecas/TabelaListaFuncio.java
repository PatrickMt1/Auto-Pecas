        jTFuncionario.getColumnModel().getColumn(2).setResizable(false);
        jTFuncionario.getColumnModel().getColumn(3).setPreferredWidth(150);
        jTFuncionario.getColumnModel().getColumn(3).setResizable(true);
        jTFuncionario.getColumnModel().getColumn(4).setPreferredWidth(150);
        jTFuncionario.getColumnModel().getColumn(4).setResizable(false);
        jTFuncionario.getColumnModel().getColumn(5).setPreferredWidth(150);
        jTFuncionario.getColumnModel().getColumn(5).setResizable(false);
        jTFuncionario.getColumnModel().getColumn(6).setPreferredWidth(150);
